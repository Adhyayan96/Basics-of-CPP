#include <bits/stdc++.h>
using namespace std;
// Bitwise operators

int main() {
// And operator
int a = 2 & 3;
// OR operator
int b = 2 | 3;
// Ex-OR operator
int c = 2 ^ 3;
// Complement operator
int d = ~5;
int g = ~-5;
// left shift operator
int s = 2 << 3;
// Right shift operator
int e = 2 >> 3;
cout << a << "\n" << b << "\n" << c << "\n" << d << "\n" << g << "\n"<< e << "\n" << s<< "\n";
return 0;
}